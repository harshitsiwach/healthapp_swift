import Foundation

// MARK: - Indian Food Database

struct IndianFoodItem: Identifiable, Codable, Hashable {
    let id: String
    let name: String
    let hindiName: String?
    let category: FoodCategory
    let region: String?
    let servingDescription: String
    let servingGrams: Double
    let calories: Int
    let proteinG: Double
    let carbsG: Double
    let fatG: Double
    let fiberG: Double
    let isVegan: Bool
    let isVegetarian: Bool
    let tags: [String]
    
    enum FoodCategory: String, Codable, CaseIterable {
        case breakfast = "Breakfast"
        case lunchDinner = "Lunch & Dinner"
        case snacks = "Snacks"
        case streetFood = "Street Food"
        case beverages = "Beverages"
        case sweets = "Sweets"
        case fruits = "Fruits"
        case dairy = "Dairy"
        case nonVeg = "Non-Veg"
        case regional = "Regional"
    }
}

// MARK: - Food Database Manager

final class IndianFoodDatabase {
    static let shared = IndianFoodDatabase()
    
    private(set) var items: [IndianFoodItem] = []
    
    private init() {
        items = Self.loadDatabase()
    }
    
    // MARK: - Search
    
    func search(_ query: String) -> [IndianFoodItem] {
        let lowered = query.lowercased()
        return items.filter { item in
            item.name.lowercased().contains(lowered) ||
            (item.hindiName?.lowercased().contains(lowered) ?? false) ||
            item.tags.contains(where: { $0.lowercased().contains(lowered) }) ||
            item.servingDescription.lowercased().contains(lowered)
        }
    }
    
    func items(for category: IndianFoodItem.FoodCategory) -> [IndianFoodItem] {
        items.filter { $0.category == category }
    }
    
    func items(isVegetarian: Bool) -> [IndianFoodItem] {
        items.filter { $0.isVegetarian == isVegetarian }
    }
    
    func popularItems() -> [IndianFoodItem] {
        let popularIds = [
            "roti_plain", "rice_white", "dal_tadka", "idli_3pc",
            "dosa_plain", "paratha_aloo", "poha", "upma",
            "rajma", "chole", "paneer_butter_masala", "biryani_chicken",
            "samosa_2pc", "vada_pav", "maggie", "chai"
        ]
        return items.filter { popularIds.contains($0.id) }
    }
    
